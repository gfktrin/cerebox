                    <p class="text-justify">
                        <strong>- Qual é a premiação dos concursos?</strong>
                        <br>
                        A premiação é totalmente feita em dinheiro, de acordo com o número de participantes do concurso. O cálculo é baseado em porcentagens já fixadas, onde concursos com um maior número de inscritos premiarão os melhores colocados com valores mais altos (e atrativos!). A equipe Cerebox planeja, futuramente, oferecer parcerias e participações de lucro, além do dinheiro para os melhores colocados. Se houver interesse por alguma arte, vencedora ou não de algum concurso, existe a possibilidade da equipe Cerebox entrar em contato com o artista para tratar sobre parcerias e negócios, portanto mantenha sempre seu email atualizado no perfil.

                    </p>
