                        <br>
                    – <strong>Criatividade</strong>; <br>
                    – <strong>Conexão com os temas</strong> (encaixar de forma prudente o uso das três palavras sugeridas no concurso);<br>
                        – <strong>Interação dos elementos</strong> (arte que correlaciona todos os elementos presentes, que não deixa nada solto só para afirmar que o uso das palavras-chave foi feito); <br>
                        – <strong>Arte</strong> <br>
                        <br>
                    </p>
