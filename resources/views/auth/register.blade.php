17.3.6. VOTOS</strong></div><br>
<div>
Os votos serão atribuídos da seguinte forma:</div><br>

    <div>&nbsp;&nbsp;a) Cada arte possuirá 4 (quatro) categorias;</div>
    <div>&nbsp;&nbsp;b) O somatório das 4 (quatro) categorias ficará entre 12 (doze) e 15 (quinze) pontos que deverão ser distribuídos entre estas categorias;</div>
    <div>&nbsp;&nbsp;c) Cada um dessas categorias receberá uma quantidade de pontos escolhida pelo usuário votante, este número deve variar entre o mínimo (2) e o máximo (5);</div>
    <div>&nbsp;&nbsp;d) Nenhuma das quatro categorias deve permanecer sem preenchimento ou com números abaixo de (2) ou acima de (5);</div>
    <div>&nbsp;&nbsp;e) O usuário logado não poderá votar mais de uma vez em uma mesma arte.</div><br>
<div>
Outro aspecto implementado no site visando à competitividade encontra-se na etapa dos votos. No primeiro voto de cada usuário (por concurso) será solicitado um segundo voto para computar o voto inicial. Este segundo voto representa uma escolha automática do sistema, que busca um dos três últimos competidores com menos votos para evitar uma disparidade numérica de votos por competidor.</div><br><br>

<div><strong>
17.3.7. RESULTADOS</strong></div><br>
<div>
Após o término da etapa de votos o sistema automaticamente dará início a contagem de votos. Após o cálculo, o sistema liberará os resultados elegendo as colocações respectivas de cada usuário. Somente os três melhores colocados receberão as devidas premiações informadas na descrição do concurso.</div><br><br>

<div><strong>
17.3.8. PREMIAÇÃO</strong></div><br>
<div>
A equipe <strong>Cerebox</strong> entrará em contato com os ganhadores via e-mail (contato principal) ou telefone (contato secundário) para que o usuário informe os dados bancários para depósito.</div><br> 
<div>
O deposito poderá ser realizado em um prazo máximo de 10 (dez) dias após a divulgação dos vencedores.</div><br><br>

<div><strong>
17.4. USO DA IMAGEM E DE ARTES</strong></div><br>
<div>
A equipe <strong>Cerebox</strong> não possui os direitos autorais sobre as artes enviadas para os concursos da plataforma. Portanto, está vetada qualquer monetização sobre as imagens sem a prévia consulta do artista autor da obra.</div><br>
<div>
Ao aceitar este termo o artista, autor de obras enviadas para a plataforma, confirma estar ciente da possibilidade de uso de suas imagens e criações para a divulgação e promoção da plataforma em redes sociais, sites e fóruns sem que esta prática seja ilegal.
Caso seja do interesse do editor do site criar algum vínculo comercial à arte em algum momento, o mesmo entrará em contato com o artista para discutir alguma proposta formal perante a Lei.</div><br><br>


